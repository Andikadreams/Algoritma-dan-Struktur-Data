package SourceCode.Tugas;

public class Tugas2Nilai {
    String kodeNilai;
    String kodeMatakul;
    String nimMhs;
    float nilai;

    Tugas2Nilai(String kodeNilai, float nilai, String kodeMatakul, String nimMhs) {
        this.kodeNilai = kodeNilai;
        this.nilai = nilai;
        this.kodeMatakul = kodeMatakul;
        this.nimMhs = nimMhs;
    }
}
